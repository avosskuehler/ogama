    {
      int selectedTrial = 0;
      if (!this.bgwCalculateTransitions.IsBusy)
      {
        // Clear Existing Rows and Columns
        this.dgvTransitions.Rows.Clear();
        this.dgvTransitions.Columns.Clear();
